package edu.unc.clinica.domain;

/**
 * Este enum representa los roles que pueden tener los usuarios en el sistema.
 * Los roles disponibles son ADMIN y USER.
 */
public enum Role {
	/**
     * El rol de administrador que tiene acceso completo al sistema.
     */
	ADMIN,
	
	 /**
     * El rol de usuario regular que tiene acceso limitado al sistema.
     */
	USER	
}
